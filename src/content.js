/**
 * 從多個缺數中判斷「最關鍵」的那一個。
 *
 * 依據：缺數所屬的五行，如果剛好是命盤中最弱的五行，
 * 代表這個弱點是雙重的——先天沒有這個數字，後天五行也不支持，
 * 影響會比其他缺數明顯。
 *
 * 加權：
 *   五行越弱 → 分數越高（主要依據）
 *   若該五行是「生我」者（貴人五行）→ 再加權，
 *   因為缺乏支撐自己的特質，比缺乏其他特質更難靠自己補上。
 */
function pickPrimaryMissing(missing, elementPct, beiSheng, elementMap) {
  if (!missing || !missing.length) return null;
  const scored = missing.map((n) => {
    const el = elementMap[n];
    let score = 100 - (elementPct[el] ?? 20);
    if (el === beiSheng) score += 15;
    return { num: n, el, score };
  });
  scored.sort((a, b) => b.score - a.score || a.num - b.num);
  return scored[0];
}

